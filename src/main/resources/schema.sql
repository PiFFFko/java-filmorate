CREATE TABLE IF NOT EXISTS LIKES (
    LIKE_ID BIGINT generated by default as identity PRIMARY KEY,
    FILM_ID BIGINT,
    USER_ID BIGINT,
    CONSTRAINT LIKES_PK PRIMARY KEY (LIKE_ID),
    CONSTRAINT LIKES_FILMS_FILM_ID_FK FOREIGN KEY (FILM_ID) REFERENCES FILMS ON DELETE cascade,
    CONSTRAINT LIKES_USERS_USER_ID_FK FOREIGN KEY (USER_ID) REFERENCES USERS ON DELETE cascade
);
CREATE INDEX IF NOT EXISTS LIKES_FK_1_INDEX_4 ON PUBLIC.LIKES (USER_ID);
CREATE INDEX IF NOT EXISTS LIKES_FK_INDEX_4 ON PUBLIC.LIKES (FILM_ID);
CREATE UNIQUE INDEX IF NOT EXISTS PRIMARY_KEY_45 ON PUBLIC.LIKES (FILM_ID,USER_ID);


-- PUBLIC.FILM_CATEGORY definition
CREATE TABLE IF NOT EXISTS FILM_CATEGORY
(
    FILM_ID  INTEGER NOT NULL,
    GENRE_ID INTEGER NOT NULL,
    CONSTRAINT FILM_CATEGORY_PK PRIMARY KEY (GENRE_ID, FILM_ID),
    CONSTRAINT FILM_CATEGORY_FK FOREIGN KEY (GENRE_ID) REFERENCES PUBLIC.GENRES (GENRE_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FILM_CATEGORY_FK_1 FOREIGN KEY (FILM_ID) REFERENCES PUBLIC.FILMS (FILM_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

-- PUBLIC.DIRECTORS definition
CREATE TABLE IF NOT EXISTS DIRECTORS
(
    ID   INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME CHARACTER VARYING(100) NOT NULL
);

-- PUBLIC.FILM_DIRECTOR definition
CREATE TABLE IF NOT EXISTS FILM_DIRECTOR
(
    FILM_ID     INTEGER,
    DIRECTOR_ID INTEGER,
    CONSTRAINT FILM_DIRECTOR_FK FOREIGN KEY (FILM_ID) REFERENCES PUBLIC.FILMS (FILM_ID) ON DELETE RESTRICT ON UPDATE RESTRICT,
    CONSTRAINT FILM_DIRECTOR_FK_1 FOREIGN KEY (DIRECTOR_ID) REFERENCES PUBLIC.DIRECTORS (ID) ON DELETE RESTRICT ON UPDATE RESTRICT
);

-- CREATE Reviews table, reviews likes, dislikes

CREATE TABLE IF NOT EXISTS REVIEWS (
    review_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content VARCHAR(200) NOT NULL,
    is_positive BOOLEAN NOT NULL,
    user_id INTEGER REFERENCES USERS(USER_ID),
    film_id INTEGER REFERENCES FILMS(FILM_ID),
    useful INTEGER NOT NULL
);
CREATE INDEX IF NOT EXISTS FILM_CATEGORY_FK_1_INDEX_4 ON PUBLIC.FILM_CATEGORY (FILM_ID);
CREATE INDEX IF NOT EXISTS FILM_CATEGORY_FK_INDEX_4 ON PUBLIC.FILM_CATEGORY (GENRE_ID);
CREATE UNIQUE INDEX IF NOT EXISTS PRIMARY_KEY_4E ON PUBLIC.FILM_CATEGORY (FILM_ID,GENRE_ID);

CREATE TABLE IF NOT EXISTS FILMS_GENRES (
    FILMS_GENRE_ID BIGINT generated by default as identity PRIMARY KEY,
    FILM_ID        BIGINT,
    GENRE_ID       INTEGER,
    CONSTRAINT FILMS_GENRES_PK PRIMARY KEY (FILMS_GENRE_ID),
    CONSTRAINT FILMS_GENRES_FILMS_FILM_ID_FK FOREIGN KEY (FILM_ID) REFERENCES FILMS ON DELETE cascade,
    CONSTRAINT FILMS_GENRES_GENRES_GENRE_ID_FK FOREIGN KEY (GENRE_ID) REFERENCES GENRES ON DELETE cascade
);

CREATE TABLE IF NOT EXISTS REVIEWS_GRADES (
    --grade_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    review_id INTEGER REFERENCES REVIEWS(review_id) ON DELETE CASCADE,
    user_id INTEGER REFERENCES USERS(USER_ID) ON DELETE CASCADE,
    grade_type BOOLEAN,
    PRIMARY KEY(review_id, user_id)
);